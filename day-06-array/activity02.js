// array methods basics 

let numbers = [1, 2, 3, 4, 5];

// push
numbers.push(6);

console.log(numbers); // Output: [ 1, 2, 3, 4, 5, 6 ]

// pop
numbers.pop();

console.log(numbers); // Output: [ 1, 2, 3, 4, 5 ]

// shift
numbers.shift();

console.log(numbers); // Output: [ 2, 3, 4, 5 ]

// unshift
numbers.unshift(1);

console.log(numbers); // Output: [ 1, 2, 3, 4, 5 ]