// template literals

let name = "Sachin Jadhav";
let age = 20;

console.log(`Name: ${name}, Age: ${age}`); // Output: Name: Sachin Jadhav, Age: 20

// multiline string

console.log(`Hello! My name is ${name}.\nI am ${age} years old.`);
/*
     Output: 

     Hello! My name is Sachin Jadhav.
    I am 20 years old.
*/