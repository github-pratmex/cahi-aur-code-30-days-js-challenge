// different data types

// number
const num = 100;
console.log(typeof num);

// string
const str = "Hello, Brother!";
console.log(typeof str);

// boolean
const bool = true;
console.log(typeof bool);

// object
const obj = {
    firstName: "Prathamesh",
    lastName: "Hegade",
    age: 20,
    hobbies: ["reading", "swimming"]
};
console.log(typeof obj);

// array
const arr = [1, 2, 3, 4, 5];
console.log(typeof arr);

/*
    Output:
    
    number
    string
    boolean
    object
    object
*/