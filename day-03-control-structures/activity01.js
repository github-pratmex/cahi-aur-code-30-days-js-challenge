// If-else statements

// task 01
let num = 9;

if(num > 0) {
    console.log(`${num} is a positive number.`);
} 
else if(num < 0) {
    console.log(`${num} is a negative number.`);
} 
else {
    console.log(`It is a zero!`);
}

/*
    Output:

    9 is a positive number.
*/

