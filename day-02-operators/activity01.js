// arithmetic operations

// addition
 let a = 10;
 let b = 20;
 let result = a + b;

 console.log(`${a} + ${b} = ${result}`);

 // subtract
 a = 45;
 b = 32;
 result = a - b;

 console.log(`${a} - ${b} = ${result}`);

 // multiply
 a = 23.45;
 b = 45.78;
 result = a * b;

 console.log(`${a} * ${b} = ${result.toFixed(2)}`);

 // divide
 a = 56;
 b = 8;
 result = a / b;

 console.log(`${a} / ${b} = ${result}`);

 // modulus
 a = 18;
 b = 5;
 result = a % b;

 console.log(`${a} % ${b} = ${result}`);

 /*
    Output:

    10 + 20 = 30
    45 - 32 = 13
    23.45 * 45.78 = 1073.54
    56 / 8 = 7
    18 % 5 = 3
  */